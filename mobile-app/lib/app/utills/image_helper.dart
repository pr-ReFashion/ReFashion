  static String getCategoryIcon(String? name) {
    if (name == null || name.isEmpty) return icNoImage;

    final lowerName = name.toLowerCase();

    for (final entry in categoryIcons.entries) {
      if (lowerName.contains(entry.key)) {
        return entry.value;
      }
    }

    return icNoImage;
